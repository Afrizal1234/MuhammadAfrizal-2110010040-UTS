    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // CODE UNTUK BUTTON TAMBAH DATA:
        DefaultTableModel model = (DefaultTableModel) tbData.getModel();
        
        // Memeriksa apakah ada kolom input yang kosong
        if (tfId.getText().trim().equals("") || tfMerek.getText().trim().equals("") || tfTipe.getText().trim().equals("") || tfWarna.getText().trim().equals("") ||tfJumlah.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Mohon isi semua kolom yang diperlukan");
        // Menampilkan pesan peringatan jika ada kolom input yang kosong
        }else{
            // Jika semua kolom input terisi
            model.addRow(new Object[]{tfId.getText(),tfMerek.getText(),tfTipe.getText(), tfWarna.getText() ,cbSize.getSelectedItem(), tfJumlah.getText()});
            // Menampilkan pesan sukses
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
        }
        // Mengosongkan kolom input setelah operasi penambahan data
        tfId.setText("");
        tfMerek.setText("");
        tfTipe.setText("");
        tfWarna.setText("");
        cbSize.setSelectedItem("36");
        tfJumlah.setText("");
        // Mengaktifkan tombol-tombol tertentu setelah operasi penambahan data
        btnEdit.setEnabled(true);
        btnSimpan.setEnabled(true);
        btnHapus.setEnabled(true);
        btnExport.setEnabled(true);
        btnImpor.setEnabled(true);
    }//GEN-LAST:event_btnTambahActionPerformed

