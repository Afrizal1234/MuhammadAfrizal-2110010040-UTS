    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
         // TODO add your handling code here:
        try {
            BufferedWriter bw = new BufferedWriter(new FileWriter("Export.txt"));
            
            for(int i = 0; i < tbData.getRowCount(); i++) { //membaca rows table
                for(int j = 0; j < tbData.getColumnCount(); j++) { //membaca column table
                    bw.write(tbData.getValueAt(i, j).toString()+" ");
                }
                bw.newLine();
            }
            
           JOptionPane.showMessageDialog(null, "Data Berhasil Export");
           bw.close();
        } catch (Exception e) {
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnExportActionPerformed

    private void btnImporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImporActionPerformed
        // TODO add your handling code here:
        try {
            BufferedReader br = new BufferedReader(new FileReader("Import.txt"));
            
            DefaultTableModel model = (DefaultTableModel) tbData.getModel();
            Object[] lines = br.lines().toArray();
            
            for (int i = 0; i < lines.length; i++) {
                String[] row = lines[i].toString().split(" ");
                model.addRow(row);
                JOptionPane.showMessageDialog(null, "Data Berhasil Import");
            }
           
        } catch (Exception e) {
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnImporActionPerformed

    private void tfIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfIdKeyReleased
        // TODO add your handling code here:
        // Memeriksa apakah JTextField tfId memiliki teks yang lebih dari 0 karakter
        if (tfId.getText().length()>0){
            // Jika ya (tfId tidak kosong):
            // Menonaktifkan beberapa tombo
            btnEdit.setEnabled(false);
            btnSimpan.setEnabled(false);
            btnHapus.setEnabled(false);
            btnExport.setEnabled(false);
            btnImpor.setEnabled(false);            
        }else{
            // Jika tidak (tfId kosong):
            // Mengaktifkan beberapa tombol
            btnEdit.setEnabled(true);
            btnSimpan.setEnabled(true);
            btnHapus.setEnabled(true);
            btnExport.setEnabled(true);
            btnImpor.setEnabled(true);
        }
    }//GEN-LAST:event_tfIdKeyReleased

